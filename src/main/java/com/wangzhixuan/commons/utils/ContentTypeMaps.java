	public static String ContentType(File file) {
		String fileName = file.getName();  
        String suffix = fileName.substring(fileName.lastIndexOf(".")).toLowerCase();
        return maps.get(suffix)==null ? maps.get(".*"):maps.get(suffix);
	}
